            </ul>



外營力作用：發生在地球的表面，藉由空氣、風或水來塑造改變地表的地貌
<table class="custom-table">
      <tr>
         <th>風化</th>
         <td>
            <ul>
               <li>
                  指地表岩石受空氣、水或生物的影響，在原地緩慢發生變化
               </li>
               <li>
                  物理風化：破壞岩石的結構，使其崩解成碎屑
               </li>
               <li>
                  化學風化：岩石和水、空氣發生化學反應使其成分改變變得鬆軟
               </li>
            </ul>
         </td>
      </tr>
      <tr>
         <th>崩壞</th>
         <td>
            <ul>
               <li>
                  風化物質受重力作用，產生向低處移動的現象，稱為崩壞
               </li>
               <li>
                  坡度陡峻、暴雨多、風化物質豐富，或水土保持不佳、缺乏植被保護的地區，較易發生崩壞
               </li>
               <li>
                  土石流、地滑(走山)、山崩(例如蘇花公路坍方)
               </li>
            </ul>
         </td>
      </tr>
      <tr>
         <th>侵蝕、搬運、堆積</th>
         <td>
            <ul>
               <li>
                  風化、崩壞作用後的鬆散岩石碎屑，容易受到河水、風、海浪等力量的侵蝕，並將其從一地搬運到另一地堆積
               </li>
               <li>
                 侵蝕、搬運與堆積作用，是一種連續性的過程，不斷地將地表的高處削平，低處填高，改變原本的形貌
               </li>
               <li>
                  環境不同，侵蝕、搬運和堆積作用的主要外營力媒介也不一樣
               </li>
               <li>
                  濕潤地區：雨水、河流
               </li>
               <li>
                  乾燥地帶：風
               </li>
               <li>
                  海岸地區：波浪、海流、潮汐
               </li>
            </ul>
         </td>
      </tr>
   </table>

