   <h5>空間資料的儲存：</h5>
   <table class="custom-table">
      <tr>
         <td>
            向量模式
         </td>
         <td>
            <ul>
               <li>將真實的地理現象，簡化成抽象的點、線、面等元素，並用（X , Y）連續座標組的方式表示。 </li>
               <li>
                  例如：一棟建築物(點的資料)，一條道路(線的資料)，公園範圍(面的資料)
               </li>
               <li>
                  向量模式的資料精確度較高，資料的管理與查詢較為方便，適用於小地域範圍、精確度要求較高的空間資料處理，如都市空間資料的儲存與呈現等
               </li>
            </ul>
         </td>
      </tr>
      <tr>
         <td>
            網格模式
         </td>
         <td>
            <ul>
               <li>將地表現象切割成若干個網格，每個網格皆被視為均質的單元，再由這些網格合起來表示真實世界的地理現象
               
               </li>
               <li>
                  網格是最小的空間單位。其大小代表資料解析度的高低；格子愈小，資料量愈龐大，圖形愈清晰，資料愈精確。
               </li>
               <li>
                  例如：衛星影像、航空照片等，均是屬於網格資料
                  
               </li>
               <li>
                  衛星影像的空間解析度：指衛星影像中的一個點在地面所占實際面積的大小。面積愈小，解析度愈大，資料的數量也隨之愈大。
                  
               </li>
               <li>
                  網格模式的資料精確度相對較低，但容易配合衛星影像從事疊圖分析和空間分析，適用於處理地表分布連續但界線不明顯的地理現象，如森林監控、汙染擴散分析等。
               
               </li>
            </ul>
         </td>
      </tr>
   </table>
   <h5>向量模式與網格模式對比：</h5>
   <table class="custom-table">
      <tr>
