   鋒面：
   <ul>
      <li>
         性質不同之冷暖兩氣團相遇時，其交界處為一不連續面，稱為鋒面
      </li>
      <li>
         在鋒面兩側空氣性質，諸如溫度、濕度、風、天氣等通常均有明顯的差異
      </li>
   </ul>

   


