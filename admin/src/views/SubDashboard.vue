   <h5>河流均夷作用</h5>
   <ul>
      <li>
         經過長時間的河水作用後，河床的高低落差逐漸減少(瀑布、急流消失)的過程
      </li>
      <li>
         均夷剖面：源頭到河口的河川縱剖面呈現圓滑凹形曲線。當河床的侵蝕和堆積達到了平衡狀態，水流動力正好消耗在搬運水中泥沙和克服水流摩擦力，這時河流縱剖面將呈現一條圓滑下凹曲線，稱為均夷剖面
      </li>
      <li>
         均夷河：河流的侵蝕和堆積處於平衡狀態的河流
      </li>
   </ul>
   


