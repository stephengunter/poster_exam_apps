   <h5>
      侵入型與噴出型火山作用
   </h5>
    <ul>
      <li>
         岩漿侵入岩層使地層隆起，並在地殼內部凝固成岩石，待上方覆蓋的地層被侵蝕殆盡後，才露出地表，如侵入岩。
      </li>
      <li>
         岩漿噴發流出地表，其噴發的形式有寧靜式和猛烈式二種
      </li>
      <li>
         寧靜式噴發：
            <ul>
               <li>
                  噴出的熔岩為基性熔岩，黏度小而流動性大，氣體容易散逸，所以沒有爆裂現象
               </li>
               <li>
                  熔岩流動快速，往往造成大面積的熔岩覆蓋，形成平坦的熔岩台地(如澎湖)或熔岩高原
               </li>
               <li>
                  寧靜式噴發的火山危險性較低，人們較容易接近，故又被形容為「可以開車一遊的火山」
               </li>
            </ul>
      </li>
      <li>
         猛烈式噴發：
         <ul>
               <li>
                  噴出的熔岩為酸性熔岩，黏度大而流動性小，氣體不容易散逸，積聚過多氣體所產生的壓力，常造成爆裂噴發
               </li>
               <li>
                  噴發時，噴發口四周的岩石被爆破拋出，熔岩和大小岩石碎屑物堆積成錐狀火山
               </li>
               <li>
                  噴發後，火山錐頂上凹陷成為火山口，火山口若積水則成火口湖
               </li>
            </ul>
      </li>
   </ul>

