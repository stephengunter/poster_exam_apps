      </li>
      <li>
         月均溫：一個月中每天日均溫總和的平均值
      </li>
      <li>
         年均溫：一年中每個月月均溫總和的平均值
      </li>
      <li>
         年溫差：最暖月均溫與最冷月均溫的差
      </li>
      <li>
         年降水量：一年中每個月降水量的總和
      </li>
   </ul>

   鋒面類型：
   <ul>
      <li>
         冷鋒：冷空氣移向暖空氣所形成的鋒面
      </li>
      <li>
         暖鋒：暖空氣移向冷空氣所形成的鋒面
      </li>
      <li>
