   <h5>
      人口轉型：人口由高出生率、高死亡率逐漸轉變為低出生率、低死亡率的過程
   </h5>
   <ul>
      <li>
         高穩定階段：高出生率，高死亡率，人口少而穩定
      </li>
       <li>
         早期擴張階段：出生率提高，死亡率下降，人口快速成長
      </li>
       <li>
         晚期擴張階段：出生率下降，死亡率低，人口持續成長
      </li>
      <li>
         低擴張階段：出生率低，死亡率低，人口數近乎停滯或負增長
      </li>
   </ul>
   


   <ul>
      <li>
         自然增加 = 出生人口 - 死亡人口
      </li>
      <li>
         社會增加 = 移入人口 - 移出人口
      </li>
      <li>
         封閉型人口系統：人口增減受自然增加主控
      </li>
      <li>
          開放型人口系統：人口增減同時受自然增加與社會增加所影響
      </li>
      <li>
         人口成長率 ＝ 自然增加率＋社會增加率
      </li>
   </ul>
