               </li>
               <li>
                  當冷、暖兩團空氣相遇時，較輕的暖空氣易沿著兩者的交界面爬升而凝雲致雨，稱為鋒面雨
               </li>
               <li>
                  鋒面雨特性：雨區廣，雨時長。例如臺灣五、六月間的梅雨。『黃梅時節半陰晴』『清明時節雨紛紛』
               </li>
               <li>
                  在熱帶洋面上產生的熱帶氣旋，當中心最大風速達到每秒17.2公尺時，即稱為颱風
               </li>
               <li>
                  颱風的潮濕氣流強烈輻合上升，凝結成濃厚雲層，而在所經地區造成的大量降水，稱為颱風雨。『日沒胭脂紅，無雨也有風』『六月十九，無風，水也吼』
               </li>
            </ul>
         </td>
      </tr>
   </table>
   
   <h5>影響氣候的主要因素</h5> 
   <table class="custom-table">
      <tr>
         <th>
            氣溫
         </th>
         <td>
            <ul>
               <li>
                  氣溫指一地的空氣溫度，即空氣中所含熱量的多寡
               </li>
               <li>
                  當空氣獲得熱量時，氣溫就升高；反之，空氣失去熱量時，氣溫就隨之降低
               </li>
               <li>
                  在對流層中，空氣的熱量主要來自地表吸收太陽輻射後，轉化成的地面輻射
               </li>
            </ul>
         </td>
      </tr>
      <tr>
         <th>
            緯度
         </th>
         <td>
            <ul>
               <li>
                  低緯度地區：太陽直射，氣溫高
               </li>
               <li>
                  高緯度地區：太陽斜射，氣溫低
               </li>
               <li>
                  緯度越高，氣溫越低。緯度與氣溫成反比
               </li>
               <li>
                  緯度越高，溫差越大。
               </li>
            </ul>
         </td>
      </tr>
       <tr>
         <th>
            距海遠近
         </th>
         <td>
            <ul>
               <li>
                  陸地：比熱小，氣溫變化大
               </li>
               <li>
                  海洋：比熱大，氣溫變化小
               </li>
               <li>
                  溫差沿海往內陸逐漸遞增。離海越遠，溫差越大。
               </li>
            </ul>
         </td>
      </tr>
      <tr>
         <th>
            洋流
         </th>
         <td>
            <ul>
               <li>
                  海洋表層的海水，長年沿著固定的方向流動，稱為洋流
               </li>
               <li>
                  暖流：溫暖濕潤(大氣不穩定，易產生對流)
               </li>
               <li>
                  寒流：寒冷乾燥
               </li>
            </ul>
         </td>
      </tr>
      <tr>
         <th>
            高度
         </th>
         <td>
            <ul>
               <li>
                  高度越高，溫度越低
               </li>
               
            </ul>
         </td>
      </tr>
      <tr>
         <th>
            植被
         </th>
         <td>
            <ul>
               <li>
                  如果地表有茂密的植被保護，如森林，則氣溫變化較趨緩和
               </li>
               <li>
                  如果地面是裸露的沙漠，則氣溫變化劇烈
               </li>
               
            </ul>
         </td>
      </tr>
      <tr>
         <th>
            氣壓與風
         </th>
         <td>
            <ul>
               <li>
                  氣壓：指單位面積所承受的空氣重量，通常以「百帕」（hPa）為單位
               </li>
               <li>
                  海拔高度越高，空氣密度和重量越小，氣壓值越低(海拔越高，氣壓越低)
               </li>
               <li>
                  在相同高度，氣壓的高低和氣溫的冷熱成反比(氣溫越高，氣壓越低)
               </li>
               <li>
                  風：是空氣由高壓區往低壓區水平流動的現象
               </li>
               
            </ul>
         </td>
      </tr>
      <tr>
         <th>
            降水
         </th>
         <td>
            <ul>
               <li>
                  定義：空氣中的水氣凝結，以液態(降雨)或固態(降雪)形式降落至地表
               </li>
               <li>
                  雨量變率 = (某年雨量 - 長期年平均雨量) ÷ 長期年平均雨量 × 100%
               </li>
               <li>
                  假設某地區過往20年平均雨量為1200毫米，今年下了1600毫米的雨，則今年的雨量變率 = (1600 - 1200) ÷ 1200 × 100% = 33.33%
               </li>
               <li>
                  通常濕潤區的年雨量相對變率較小，乾燥區相對較大
               </li>
               <li>
                  年雨量相對變率對農業生產影響甚大，雨量變率小，表示每年降雨量穩定，農業收成可靠；反之，則易造成水、旱災，導致農產歉收
               </li>
               
            </ul>
         </td>
      </tr>
   </table>
   
