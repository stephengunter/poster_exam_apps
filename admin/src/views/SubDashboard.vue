   作用臺灣地形的力量：
   <ul>
               <li>
                 內營力：臺灣經由歐亞大陸板塊及菲律賓海板塊長期的擠壓及火山運動所形成，如中央山脈、花東縱谷、大屯火山群等，主要控制了臺灣大地形區域的呈現
               </li>
               <li>
                  外營力：暴露於地面的岩石，受到地表風化、堆積、侵蝕與崩壞作用等，形成平原、台地、丘陵等地形景觀
