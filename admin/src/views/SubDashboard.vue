   <h5>
      海水作用
   </h5>
   <ul>
      <li>
         海水以波浪、海流、潮汐等運動形式，持續在海岸地區進行侵蝕、搬運、堆積等作用
      </li>
      <li>
         海蝕地形：波浪對海岸的破壞力最強，常挾其動能拍擊海岸，然後向後撤退，在前衝後退的過程中，海水攜帶泥沙、石礫磨蝕海岸地區，形成海蝕地形
      </li>
      <li>
         海積地形：由波浪侵蝕下來的泥沙與岩屑，則再經波浪、潮汐或海流，輸送到沿海適當的地點堆積，形成海積地形
      </li>
   </ul>
   <h5>
      河川襲奪
   </h5>
   <ul>
      <li>
         相鄰的兩條河流，在發育的過程中，低位河因向源侵蝕而使河谷上源與高位河相遇，進而襲奪其河水的現象，稱為河川襲奪
      </li>
      <li>
         此時低位河稱為襲奪河，而被襲奪的河川，其上游河段改向流入襲奪河，稱改向河。其下游河段則因源頭被奪，稱斷頭河
      </li>
      <li>
         襲奪河因流量增加，改向河因侵蝕基準下移，侵蝕力皆增強，出現回春作用。斷頭河則因水量減少，堆積較盛
      </li>
   </ul>
