   <h5>經度與時區</h5>
   <ul>
      <li>
         原理：由於地球由西向東自轉，各地照到太陽的時間前後不一。
      </li>
      <li>
         從0度經線開始，向東每隔經線15度，時間增加一小時。
      </li>
      <li>
         從0度經線開始，向西每隔經線15度，時間減少一小時。
      </li>
      <li>
         全球共24個時區，其中0度經線即『格林威治』時間。
      </li>
      <li>
         範例：英國倫敦(經度0)時間8點，則台北(120°E)時間為16點。美國舊金山(120°W)則為0點。
