   都會區：交通發達與產業發達，使聚落之間的往來更加便捷與頻繁，加速人口的移動，使得都市人口增多，都市範圍不斷擴大與鄰近都市之間的界線愈來愈模糊，漸漸連成一體，形成「都會區」。
  

都市化
<ul>
<li>由於都市提供較多的就業機會，與較便利的生活環境，因而吸引鄉村人口往都市集中，此一人口移動的過程，稱為「都市化」</li>
<li>的都市人口占人口總數的比例愈高，代表當地工業、服務業發展程度愈高，經濟水準也較佳</li>
<li>
   臺灣都市的人口接近總人口80％，都市化程度高
</li>
<li>
   都市化問題
   <ul>
      <li>
         鄉村因人口流失，造成勞力不足與人口老化等現象
      </li>
      <li>
         都市地區因人口過多，造成交通壅塞、環境汙染、房價過高、公共建設不足等現象
      </li>
   </ul>
</li>
      
</ul>

   相互依存
   <ul>
   <li>
   鄉村提供農產品供應都市居民日常所需，田野景觀是都市居民遊憩的去處
   </li>
    <li>
    都市工業、服務業發達，提供鄉村居民較多的消費選擇和就業機會
   </li>        
         
</ul>
   
   鄉村聚落：人口數較少、人口密度較低，居民以從事第一級產業為主
   都市聚落：人口數較多、人口密度較高，大多數居民從事第二、三級產業為主
<ul>
   <li>
   鄉村聚落：與自然環境有關，如水源充足、地勢平坦、農漁業資源豐富的地方，形成具有糧食生產機能的鄉村聚落
   </li>
    <li>
    都市聚落：因礦產資源豐富或交通位置重要，工商業發展迅速，逐漸形成都市聚落
   </li>        
         
</ul>
   國際分工角色
   <ul>
     <li>
        發展基礎：
        <ul>
         <li>
           自民國60年代，臺灣的出口產品增多，國際貿易大多為出超 
         </li>
            <li>
           近年來，臺灣的電子、資訊等高科技工業產品在國際上居重要地位
         </li>
            
         
         </ul>
      </li>
       <li>
        扮演角色：代為加工生產高技術的產品零件，如主機板、液晶螢幕等電腦零件與周邊產品，供應許多國際大廠的需求
      </li>
       <li>
        發展重心：
        <ul>
         <li>
           隨著經濟發展，國民所得增加，臺灣的勞力成本提高，部分勞力密集度高的產業(如成衣、製鞋工業)逐漸外移
         </li>
            <li>
           臺灣工業逐改以產品設計與技術研發為重心，朝自創品牌努力(如華碩、宏碁、宏達電、捷安特..等)
         </li>
             <li>
          自創品牌為臺灣進軍國際市場增強競爭力，並且獲取較代工更高的利潤，也促進國家經濟的成長
         </li>
          
         </ul>
      </li>
      
      
   
   </ul>


